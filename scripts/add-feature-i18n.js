function mergeMissing(target, additions) {
  for (const [k, v] of Object.entries(additions)) {
    if (v && typeof v === 'object' && !Array.isArray(v)) {
      if (!target[k] || typeof target[k] !== 'object') target[k] = {};
      mergeMissing(target[k], v);
    } else if (!(k in target)) {
      target[k] = v;
    }
  }
}

