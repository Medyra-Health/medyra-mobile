let ok = 0;
for (const [locale, dict] of Object.entries(T)) {
  const file = path.join(DIR, `${locale}.json`);
  const data = JSON.parse(fs.readFileSync(file, 'utf8'));
  data.tabs = data.tabs || {};
  data.tabs.meds = dict.tab;
  data.features = data.features || {};
  data.features.meds = dict.feature;
  data.features.medsDesc = dict.featureDesc;
  data.medplan = dict.medplan;
  fs.writeFileSync(file, JSON.stringify(data, null, 2) + '\n', 'utf8');
  ok++;
}
console.log(`medplan i18n injected into ${ok} locales`);
